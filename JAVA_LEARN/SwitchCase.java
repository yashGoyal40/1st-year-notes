public class SwitchCase {
    public static void main(String[] args){
        int a =4;
        switch(a){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default: 
                System.out.println("4");
                break;
        }
    }

}
