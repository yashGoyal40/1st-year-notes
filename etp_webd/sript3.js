let a=parseInt(prompt("enter day"))
switch(a){
    case 1:
        alert("mon")
        break
    case 2:
        alert("tue")
        break
    case 3:
        alert("wed")
        break
    case 4:
        alert("thus")
        break
    case 5:
        alert("fri")
        break
    case 6:
        alert("sat")
    case 7:
        alert("sun")
    default:
        alert("enter valid day")
}