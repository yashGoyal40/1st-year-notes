public class DataTypes {
    //primitive 
    public static void main(String[] args) {
        int a = 4;
        String s = "char";
        char c = 'c';
        boolean b = true;
        float f = 10.4f;
        double d = 2.112;

        System.out.println(a+s+c+b+f+d);
        System.out.printf("%010.2f",d);
    }
}
